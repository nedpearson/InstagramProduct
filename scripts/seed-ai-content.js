async function seedContent() {
  console.log('🚀 Starting AI content seeding...\n');

  // Find the active product/campaign
  const product = await prisma.product.findFirst({ orderBy: { createdAt: 'desc' } });
  if (!product) { console.error('No product found'); process.exit(1); }

  const campaign = await prisma.campaign.findFirst({
    where: { status: 'active' },
    orderBy: { createdAt: 'desc' }
  });
  if (!campaign) { console.error('No active campaign found'); process.exit(1); }

  console.log(`📦 Product: ${product.name}`);
  console.log(`🎯 Campaign: ${campaign.name}\n`);

  let totalAssets = 0;
  let totalVariants = 0;
  const scheduledDates = [];

  // Find last scheduled date to continue from
  const lastScheduled = await prisma.schedule.findFirst({
    orderBy: { scheduledFor: 'desc' }
  });
  
  let nextDate = new Date();
  if (lastScheduled) {
    nextDate = new Date(lastScheduled.scheduledFor);
    nextDate.setDate(nextDate.getDate() + 1);
  } else {
    nextDate.setDate(nextDate.getDate() + 1);
  }
  nextDate.setHours(9, 0, 0, 0);

  for (const content of CONTENT_PACK) {
    // Create the ContentAsset
    const asset = await prisma.contentAsset.create({
      data: {
        campaignId: campaign.id,
        title: content.title,
        assetType: content.assetType,
        status: 'approved',
      }
    });

    console.log(`✅ Created asset: "${content.title}" [${content.assetType}]`);
    totalAssets++;

    // Create variants
    for (const v of content.variants) {
      const fullBody = v.hook + '\n\n' + v.body + (v.cta ? '\n\n' + v.cta : '');
      
      const variant = await prisma.assetVariant.create({
        data: {
          assetId: asset.id,
          variantTag: v.variantTag,
          hook: v.hook,
          body: fullBody,
        }
      });

      // Schedule the first variant of each asset
      if (content.variants.indexOf(v) === 0) {
        const scheduledFor = new Date(nextDate);
        await prisma.schedule.create({
          data: {
            variantId: variant.id,
            scheduledFor,
            status: 'pending'
          }
        });
        
        await prisma.contentAsset.update({
          where: { id: asset.id },
          data: { status: 'scheduled' }
        });

        scheduledDates.push({ title: content.title, date: scheduledFor.toDateString() });
        nextDate.setDate(nextDate.getDate() + 1);
      }

      console.log(`   → Variant [${v.variantTag}]: ${v.hook.substring(0, 60)}...`);
      totalVariants++;
    }
  }

  const totalSchedules = await prisma.schedule.count();
  
  console.log('\n═══════════════════════════════════════════════════');
  console.log('🎉 CONTENT SEEDING COMPLETE');
  console.log('═══════════════════════════════════════════════════');
  console.log(`  Assets created:    ${totalAssets}`);
  console.log(`  Variants created:  ${totalVariants}`);
  console.log(`  Total on calendar: ${totalSchedules}`);
  console.log('\n📅 NEW SCHEDULE ADDITIONS:');
  scheduledDates.forEach((s, i) => console.log(`  ${i + 1}. ${s.title} → ${s.date}`));
  console.log('═══════════════════════════════════════════════════\n');

  await prisma.$disconnect();
}

seedContent().catch(e => { console.error('Error:', e.message); process.exit(1); });
